export function LanguageSwitcher() {
  const { i18n } = useTranslation();

  const currentLang = languages.find(lang => lang.code === i18n.language) || languages[0];

  const changeLanguage = (langCode: string) => {
    i18n.changeLanguage(langCode);
    localStorage.setItem('language', langCode);
  };

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button 
          variant="outline" 
          size="sm" 
          className="gap-2 h-9 px-3 bg-secondary border-primary/40 hover:bg-primary/20 hover:border-primary transition-all"
        >
          <Globe className="h-4 w-4 text-primary" />
          <span className="text-lg leading-none">{currentLang.flag}</span>
          <span className="hidden sm:inline text-xs font-semibold uppercase tracking-wide text-foreground">
            {currentLang.code}
          </span>
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="min-w-[140px]">
        {languages.map((lang) => (
          <DropdownMenuItem
            key={lang.code}
            onClick={() => changeLanguage(lang.code)}
            className={cn(
              'gap-3 cursor-pointer',
              lang.code === i18n.language && 'bg-primary/10'
            )}
          >
            <span className="text-lg">{lang.flag}</span>
            <span className="flex-1 font-medium">{lang.name}</span>
            {lang.code === i18n.language && (
              <Check className="h-4 w-4 text-primary" />
            )}
          </DropdownMenuItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  );
}
